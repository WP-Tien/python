number: int = 10
print( type(number) )

decimal: float = 2.5
print( type(decimal) )

text: str = "Hello, world!"
print( type(str) )

active: bool = True
print( type(active) )

names: list = [ 'Vincent', 'Sarah', 'Marry' ]
